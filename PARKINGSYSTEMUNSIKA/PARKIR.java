    private void BTNKeluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNKeluarActionPerformed
        try {
            String noTiket = TFNoTiket1.getText();
            String jamKeluar = TFJamK1.getText(); // Format: "yyyy-MM-dd HH:mm:ss"

            ParkingDAO dao = new ParkingDAO();

            // Ambil data berdasarkan nomor tiket
            ParkingData data = dao.getParkingDataByTicket(noTiket);
            if (data == null) {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan untuk No Tiket: " + noTiket);
                return;
            }

            // Isi field otomatis
            TFNPM1.setText(data.getNpm());
            TFNama1.setText(data.getNama());
            TFNoPol1.setText(data.getNoPolisi());
            CMBKendaraan1.setSelectedItem(data.getJenisKendaraan());
            TFJamM1.setText(data.getJamMasuk());

            // Hitung durasi dan biaya
            CalculationResult result = dao.calculateParking(data.getJamMasuk(), jamKeluar, data.getJenisKendaraan());
            TFDurasi.setText(result.getDurationHours() + " Jam");
            TFBiaya.setText("Rp" + result.getTotalCost());

            // Update database
            dao.updateParkingData(noTiket, jamKeluar, result.getDurationHours(), result.getTotalCost());

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_BTNKeluarActionPerformed

